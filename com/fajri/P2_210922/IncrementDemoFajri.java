public class IncrementDemoFajri {
    public static void main(String[] args)
    {
        int i = 10;
        int j = 3;
        int k = 0;
        k = ++j + i; //akan menghasilkan k = 4+10 = 14
        
        System.out.println("Nilai k = " +k);
        System.out.println("Nilai k = " +j);
    }
    
}
